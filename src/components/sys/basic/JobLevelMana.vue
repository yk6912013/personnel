<template>
    <div>
        <div style="display: flex;justify-content: flex-start">
            <el-input v-model="jl.name" style="width: 300px;" icon="el-icon-edit" placeholder="添加职称..."></el-input>
            <el-select v-model="jl.titlelevel" placeholder="职称等级" style="margin-left: 10px;margin-right: 10px">
                <el-option
                    v-for="item in titlelevels"
                    :key="item"
                    :label="item"
                    :value="item">
                </el-option>
            </el-select>
            <el-button icon="el-icon-plus" type="primary" @click="addJobLevel">添加</el-button>
            <el-input
                style="margin-left: 10px;margin-right: 10px"
                class="addPosInput"
                placeholder="请输入职称名称进行搜索..."
                prefix-icon="el-icon-search"
                v-model="jl.searchname">
            </el-input>
            <el-button icon="el-icon-search" type="primary" @click="searchname">搜索</el-button>
        </div>
        <div style="margin-top: 10px">
            <el-table
                    :data="jls"
                    border
                    v-loading="loading"
                    element-loading-spinner="el-icon-loading"
                    element-loading-text="正在拼命加载中..."
                    element-loading-background="rgba(0, 0, 0, 0.8)"
                    size="medium"
                    stripe
                    max-height="680"
                    style="width:100%"
                    @selection-change="handleSelectionChange"
            >
                <!-- 无数据展示 -->
                <template slot="empty">
                    <div class="empty">
                        <div>
                        <img src="@/assets/images/No-Date.png" width="240px" height="240px" alt>
                        </div>
                        <div>
                        <span>暂无数据</span>
                        </div>
                    </div>
                </template>
                <el-table-column type="selection" width="55" align="center">
                </el-table-column>
                <el-table-column prop="id" label="ID" sortable align="center" width="70">
                </el-table-column>
                <el-table-column prop="name" label="职称名称" width="150" align="center">
                </el-table-column>
                <el-table-column prop="titlelevel" label="职称级别" width="150" align="center">
                </el-table-column>
                <el-table-column prop="createdate" label="创建时间" width="200" align="center">
                </el-table-column>
                <el-table-column label="是否启用" width="150" align="center">
                    <template slot-scope="scope">
                        <el-tag type="success" size="small" v-if="scope.row.enabled">已启用</el-tag>
                        <el-tag type="danger" size="small" v-else>未启用</el-tag>
                    </template>
                </el-table-column>
                <el-table-column label="操作">
                    <template slot-scope="scope">
                        <el-button size="mini" @click="showEditView(scope.row)">编辑</el-button>
                        <el-button size="mini" type="danger" @click="deleteHandler(scope.row)">删除</el-button>
                    </template>
                </el-table-column>
            </el-table>
            <div>
                <el-button type="danger" icon="el-icon-delete" style="margin-top: 15px" :disabled="multipleSelection.length==0" @click="deleteMany">
                    批量删除
                </el-button>
                <el-button @click="refershMany" icon="el-icon-refresh" type="success">
                    刷 新
                </el-button>
            </div>
        </div>
        <el-dialog title="修改职称" :visible.sync="dialogVisible" width="30%">
            <div>
                <el-form :rules="rules">
                    <el-form-item label="职称名称" prop="updateJl.name" label-width="100px">
                        <el-input type="text" class="updatePosInput" size="small" v-model="updateJl.name"/>
                    </el-form-item>
                    <el-form-item label="职称等级" prop="updateJl.titlelevel" label-width="100px">
                        <el-select v-model="updateJl.titlelevel" placeholder="职称等级" size="small"
                                    style="margin-left: 5px;margin-right: 5px">
                            <el-option v-for="item in titlelevels" :key="item" :label="item" :value="item">
                            </el-option>
                        </el-select>
                    </el-form-item>
                    <el-form-item label="是否启用" prop="updateJl.enabled" label-width="100px">
                        <el-switch v-model="updateJl.enabled" style="margin-left: 15px">
                        </el-switch>
                    </el-form-item>
                </el-form>
            </div>
            <span slot="footer" class="dialog-footer">
    <el-button    size="small" @click="outUpdate">取 消</el-button>
    <el-button    size="small" type="primary" @click="doUpdate">确 定</el-button>
    </span>
        </el-dialog>
    </div>
</template>

<script>
    export default {
        name: "JobLevelMana",
        data() {
            return {
                dialogVisible: false,
                loading: false,
                multipleSelection: [],
                updateJl: {
                    name: '',
                    titlelevel: '',
                    enabled: false
                },
                jl: {
                    name: '',
                    titlelevel: '',
                    searchname: '',
                },
                jls: [],
                titlelevels: [
                    '正高级',
                    '副高级',
                    '中级',
                    '初级',
                    '员级',
                ]
            }
        },
        mounted() {
            this.initJls();
        },
        methods: {
            refershMany() {
                this.initJls();
            },
            searchname() {
                if (this.jl.searchname) {
                    this.$notify.success({
                        title: '搜索信息',
                        message: '搜索职位中...',
                        showClose: false,
                        offset: 100,
                        duration: 2000,
                        customClass: 'fontclass'
                    });
                    this.jl.name = this.jl.searchname;
                    this.postRequest("/system/basic/joblevel/serch/", this.jl).then(resp => {
                        if (resp) {
                            /*更新数据+清空列表*/
                            this.jls = resp;
                            this.jl.searchname = '';
                            this.jl.name = '';
                        }
                    })
                } else {
                    this.$notify({
                        title: '搜索信息',
                        message: '搜索字段不可以为空!',
                        showClose: false,
                        offset: 100,
                        duration: 2000,
                        customClass: 'fontclass',
                        type: 'warning'
                    });
                }
            },
            deleteMany() {
                this.$confirm('此操作将永久删除【' + this.multipleSelection.length + '】条记录, 是否继续?', '提示', {
                    confirmButtonText: '确定',
                    cancelButtonText: '取消',
                    type: 'warning'
                }).then(() => {
                    let ids = '?';
                    this.multipleSelection.forEach(item => {
                        ids += 'ids=' + item.id + '&';
                    })
                    this.$notify.success({
                        title: '删除信息',
                        message: '删除职位中...',
                        showClose: false,
                        offset: 100,
                        duration: 4000,
                        customClass: 'fontclass'
                    });
                    this.deleteRequest("/system/basic/joblevel/" + ids).then(resp => {
                        if (resp) {
                            this.initJls();
                        }
                    })
                }).catch(() => {
                    this.$notify.info({
                        title: '删除信息',
                        message: '已取消删除 ',
                        showClose: false,
                        offset: 100,
                        duration: 4000,
                        customClass: 'fontclass'
                    });
                });
            },
            outUpdate() {
                this.dialogVisible = false;
                this.$notify.info({
                    title: '修改信息',
                    message: '已取消修改职位 ',
                    showClose: false,
                    offset: 100,
                    duration: 2000,
                    customClass: 'fontclass'
                });
            },
            doUpdate() {
                this.$notify.success({
                    title: '修改信息',
                    message: ' 修改职位中...',
                    showClose: false,
                    offset: 100,
                    duration: 2000,
                    customClass: 'fontclass'
                });
                this.putRequest("/system/basic/joblevel/", this.updateJl).then(resp => {
                    if (resp) {
                        this.initJls();
                        this.dialogVisible = false;
                    }
                })
            },
            handleSelectionChange(val) {
                this.multipleSelection = val;
            },
            showEditView(data) {
                Object.assign(this.updateJl, data);
                this.dialogVisible = true;
            },
            deleteHandler(data) {
                this.$confirm('此操作将永久【' + data.name + '】职称, 是否继续?', '提示', {
                    confirmButtonText: '确定',
                    cancelButtonText: '取消',
                    type: 'warning'
                }).then(() => {
                    this.$notify.success({
                        title: '删除信息',
                        message: '删除职位中...',
                        showClose: false,
                        offset: 100,
                        duration: 3000,
                        customClass: 'fontclass'
                    });
                    this.deleteRequest("/system/basic/joblevel/" + data.id).then(resp => {
                        if (resp) {
                            this.initJls();
                        }
                    })
                }).catch(() => {
                    this.$notify.info({
                        title: '删除信息',
                        message: '已取消删除',
                        showClose: false,
                        offset: 100,
                        duration: 3000,
                        customClass: 'fontclass'
                    });
                });
            },
            addJobLevel() {
                if (this.jl.name && this.jl.titlelevel) {
                    this.$notify.success({
                        title: '添加信息',
                        message: '添加职位中...',
                        showClose: false,
                        offset: 100,
                        duration: 4000,
                        customClass: 'fontclass'
                    });
                    this.postRequest("/system/basic/joblevel/", this.jl).then(resp => {
                        if (resp) {
                            this.initJls();
                        }
                    });
                } else {
                    this.$notify({
                        title: '添加信息',
                        message: '添加字段不可以为空!',
                        showClose: false,
                        offset: 100,
                        duration: 4000,
                        customClass: 'fontclass',
                        type: 'warning'
                    });
                }
            },
            /*数据初始化*/
            initJls() {
                setTimeout(() => {
                    this.$notify.success({
                        title: '系统信息',
                        message: '职称信息加载中...',
                        showClose: false,
                        offset: 100,
                        duration: 2000,
                        customClass: 'fontclass'
                    });
                }, 1000);
                this.loading = true;
                this.getRequest("/system/basic/joblevel/").then(resp => {
                    // this.loading = false;
                    if (resp) {
                        this.loading = false;
                        this.jls = resp;
                        this.jl = {
                            name: '',
                            titleLevel: ''
                        };
                    }
                })
            }
        }
    }
</script>

<style scoped >
.fontclass {
    font-size: 35px;
    font-family: 站酷庆科黄油体;
}
</style>